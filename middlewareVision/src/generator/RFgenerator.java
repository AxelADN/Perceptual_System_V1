    /**
     * generar
     */
    public void generate() {
        RFlist.clearList();
        for (int i = 0; i < jTable1.getRowCount(); i++) {
            if (CompleteRow(i)) {
                RFlist.addElement((double) jTable1.getValueAt(i, 0), (double) jTable1.getValueAt(i, 1), (int) jTable1.getValueAt(i, 2), (int) jTable1.getValueAt(i, 3), (double) jTable1.getValueAt(i, 4), (double) jTable1.getValueAt(i, 5), (int) jTable1.getValueAt(i, 6));
            }
        }
    }
    
    public void getList(DefaultMutableTreeNode node, File f) {
        File fList[] = f.listFiles();
        for (File fi : fList) {
            node.add(new DefaultMutableTreeNode(fi));
        }
    }

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        int row = jTable1.getSelectedRow();
        for (int i = 0; i < jTable1.getColumnCount(); i++) {
            jTable1.setValueAt(null, row, i);
        }
        for (int i = row; i < jTable1.getRowCount(); i++) {
            for (int j = 0; j < jTable1.getColumnCount(); j++) {
                if (i < jTable1.getRowCount() - 1) {
                    jTable1.setValueAt(jTable1.getValueAt(i + 1, j), i, j);
                } else {
                    jTable1.setValueAt(null, i, j);
                }
            }
        }
        generate();
        visPanel1.repaint();
    }//GEN-LAST:event_jButton2ActionPerformed

